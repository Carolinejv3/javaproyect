    private void BTN_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_insertActionPerformed
        // TODO add your handling code here:
       String user,clave,mail,nombre;
       String rol;
         
        user = userfld1.getText();
       nombre = nombrefield.getText();
        mail = correofld.getText();
        clave = String.valueOf(passfld.getPassword());
        rol = (String) jComboBox1.getSelectedItem();
        
        PreparedStatement ps;
        ResultSet rs;
     
        String query = "INSERT INTO usuario(`ID_user`,`nombre`,`User`,`correo_electronico`,`Password`,`id_rol`) VALUES(0,?,?,?,?,?)";
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
         
            ps.setString(1, nombre);
            ps.setString(2, user);
            ps.setString(3, mail);
            ps.setString(4, clave);
            ps.setString(5, rol);
            
                  
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Agregado", "Usuario insertado",2);
                Mostrardatos("");
                Limpiar();
            }
            else{
                    JOptionPane.showMessageDialog(null, "error","lo sentimos ha ocurrido un error",2);
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(control_usuario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }//GEN-LAST:event_BTN_insertActionPerformed

